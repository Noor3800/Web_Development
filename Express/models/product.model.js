let mongoose = require ("mongoose");

let productSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    image: {
        type: String,
        required: true
    }
})

let ProductModel = mongoose.model("MyProduct", productSchema);
module.exports = ProductModel;
